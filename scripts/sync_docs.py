def _pd(v: str) -> dict:
    """Parse a Python-literal dict embedded in a CSV cell."""
    if not v:
        return {}
    try:
        return ast.literal_eval(v)
    except (ValueError, SyntaxError):
        return {}


def _read_csv(path: Path) -> list[dict]:
    if not path.exists() or path.stat().st_size <= 1:
        return []
    return list(csv.DictReader(path.open()))


def _pct(v, decimals: int = 2) -> str:
    return f"{float(v) * 100:.{decimals}f}%"


def _pct1(v) -> str:
    return f"{float(v) * 100:.1f}%"


def _ms(v) -> str:
    n = round(float(v) * 1000)
    return f"{n:,} ms" if n >= 1000 else f"{n} ms"


def _ms_no_comma(v) -> str:
    return f"{round(float(v) * 1000)} ms"


def _f(v, decimals: int = 3) -> str:
    return f"{float(v):.{decimals}f}"


def build_numeric_specs(results_dir: Path) -> list[dict]:
    """Produce a list of numeric-cell patch specs.

    Each spec is::

        {
          "card_id": str,        # <div class="card" id="…">  e.g. "stt-accuracy"
          "row_key": tuple[str], # leading <td> cell texts that locate the row
          "col": int,            # 0-based column index of the data-apr/jun cell
          "value": str,          # new value to write into data-{run}
        }

    or, for one-off labelled values (metric cards, prose-embedded spans)::

        {"card_id": str, "card_label": str, "value": str}
    """
    out: list[dict] = []

    def add(card_id, row_key, col, value, extra=None):
        spec = {"card_id": card_id, "row_key": row_key, "col": col, "value": value}
        if extra:
            spec.update(extra)
        out.append(spec)

    # ── Test 1.1 Accuracy ────────────────────────────────────────────────
    for r in _read_csv(results_dir / "stt/accuracy/summary.csv"):
        ds = r["dataset"]
        wer = _pct(r["wer"])
        cer = _pct(r["cer"]) if r.get("cer") else wer
        ser = _pct(r["ser"])
        ci = f"{_pct(r['wer_ci_lo'])}–{_pct(r['wer_ci_hi'])}"
        add("stt-accuracy", (ds,), 0, wer)
        add("stt-accuracy", (ds,), 1, cer)
        add("stt-accuracy", (ds,), 2, ser)
        add("stt-accuracy", (ds,), 3, ci)

    # ── Test 1.2 Performance ─────────────────────────────────────────────
    bucket_label = {"10": "10 seconds", "30": "30 seconds"}
    for r in _read_csv(results_dir / "stt/performance/summary.csv"):
        bs = r["bucket_s"]
        if bs not in bucket_label:
            continue
        label = bucket_label[bs]
        add("stt-performance", (label,), 0, _f(r["rtf_mean"], 4))
        add("stt-performance", (label,), 1, f"{float(r['rtfx_mean']):.1f}x")
        add("stt-performance", (label,), 2, _ms_no_comma(r["latency_p50"]))
        add("stt-performance", (label,), 3, _ms_no_comma(r["latency_p90"]))
        add("stt-performance", (label,), 4, _ms_no_comma(r["latency_p99"]))
    for r in _read_csv(results_dir / "stt/performance/concurrency_summary.csv"):
        n = r["concurrency"]
        add("stt-performance", (n,), 0, _ms_no_comma(r["p50"]))
        add("stt-performance", (n,), 1, _ms_no_comma(r["p99"]))
        add("stt-performance", (n,), 2, _f(r["rtf_mean"], 3))

    # ── Test 1.3 Streaming ───────────────────────────────────────────────
    rows = _read_csv(results_dir / "stt/streaming/summary.csv")
    if rows:
        r = rows[0]
        ttfw = _pd(r["ttfw"])
        fin = _pd(r["finalization_latency"])
        rtf = _pd(r["streaming_rtf"])
        plat = _pd(r["partial_latency"])
        stab = float(r["stability_rate_mean"])
        add_card = lambda label, val: out.append(
            {"card_id": "stt-streaming", "card_label": label, "value": val}
        )
        if ttfw: add_card("TTFW P50", f"{round(ttfw['p50'] * 1000)}")
        if ttfw: add_card("TTFW P90", f"{round(ttfw['p90'] * 1000)}")
        if fin:  add_card("Finalization P50", f"{round(fin['p50'] * 1000)}")
        if rtf:  add_card("Streaming RTF", f"{rtf['p50']:.2f}")
        add_card("Stability Rate", f"{stab * 100:.1f}")
        if plat: add_card("Partial Latency P50", f"{round(plat['p50'] * 1000)}")
        bw = float(r["batch_wer"])
        sw = float(r["stream_wer"])
        delta = sw - bw
        add("stt-streaming", ("Batch",), 0, f"{bw * 100:.2f}%")
        add("stt-streaming", ("Streaming",), 0, f"{sw * 100:.2f}%")
        add("stt-streaming", ("Streaming",), 1,
            f"{'+' if delta >= 0 else ''}{delta * 100:.2f} pp")

    # ── Test 1.4 REST vs gRPC ────────────────────────────────────────────
    mode_label = {
        "batch":           "Batch",
        "streaming_ttfw":  "Streaming TTFW",
        "streaming_final": "Streaming Final",
        "streaming_total": "Streaming Total",
    }
    iface_label = {"grpc": "gRPC", "rest": "REST"}
    for r in _read_csv(results_dir / "stt/rest_vs_grpc/summary.csv"):
        mode = mode_label.get(r["mode"], r["mode"])
        iface = iface_label.get(r["interface"], r["interface"])
        add("stt-rest-grpc", (mode, iface), 0, _ms_no_comma(r["p50"]))
        add("stt-rest-grpc", (mode, iface), 1, _ms_no_comma(r["p90"]))
        add("stt-rest-grpc", (mode, iface), 2, _ms_no_comma(r["p99"]))

    # ── Test 1.5 Noise ───────────────────────────────────────────────────
    noise_types = ["gaussian", "babble", "traffic", "office"]
    snr_columns = ["-5.0", "0.0", "5.0", "10.0", "15.0", "20.0"]
    by_type: dict[str, dict[str, str]] = {nt: {} for nt in noise_types}
    for r in _read_csv(results_dir / "stt/noise_robustness/summary.csv"):
        nt = r["noise_type"]
        if nt in by_type:
            by_type[nt][r["snr_db"]] = _pct1(r["wer"])
    label_for = {"gaussian": "Gaussian", "babble": "Babble",
                 "traffic": "Traffic", "office": "Office"}
    for nt in noise_types:
        for col_idx, snr in enumerate(snr_columns):
            v = by_type[nt].get(snr)
            if v is not None:
                add("stt-noise", (label_for[nt],), col_idx, v)

    # ── Test 1.6 Accent ──────────────────────────────────────────────────
    accent_rows = _read_csv(results_dir / "stt/accent/summary.csv")
    accent_label = {"NorthernIrish": "Northern Irish"}
    for r in accent_rows:
        label = accent_label.get(r["accent"], r["accent"])
        ci = f"{_pct(r['wer_ci_lo'])}–{_pct(r['wer_ci_hi'])}"
        add("stt-accent", (label,), 0, _pct(r["wer"]))
        add("stt-accent", (label,), 1, ci)
    if accent_rows:
        fd = float(accent_rows[0]["fairness_delta"]) * 100
        out.append({"card_id": "stt-accent", "card_label": "Fairness delta",
                    "value": f"{fd:.2f} pp"})

    # ── Test 1.8 Domain ──────────────────────────────────────────────────
    rows = _read_csv(results_dir / "stt/domain/summary.csv")
    if rows:
        r = rows[0]
        out.append({"card_id": "stt-domain", "card_label": "Domain WER",
                    "value": f"{float(r['domain_wer']) * 100:.1f}"})
        out.append({"card_id": "stt-domain", "card_label": "Key Recognition Rate",
                    "value": f"{float(r['krr_mean']) * 100:.1f}"})
        out.append({"card_id": "stt-domain", "card_label": "NE-WER",
                    "value": f"{float(r['ne_wer_mean']) * 100:.1f}"})

    # ── Test 1.9 Output ──────────────────────────────────────────────────
    metric_label = {
        "Punctuation F1 (.)":         "Punctuation F1 — Period (.)",
        "Punctuation F1 (,)":         "Punctuation F1 — Comma (,)",
        "Punctuation F1 (?)":         "Punctuation F1 — Question Mark (?)",
        "Punctuation F1 (!)":         "Punctuation F1 — Exclamation (!)",
        "Capitalisation Accuracy":    "Capitalisation Accuracy",
        "Number Formatting Accuracy": "Number Formatting Accuracy",
    }
    for r in _read_csv(results_dir / "stt/output_quality/summary.csv"):
        label = metric_label.get(r["metric"])
        if label is None:
            continue
        add("stt-output", (label,), 0, _pct1(r["value"]))

    # ── Test 1.10 Confidence ─────────────────────────────────────────────
    rows = _read_csv(results_dir / "stt/confidence/summary.csv")
    if rows:
        r = rows[0]
        out.append({"card_id": "stt-confidence", "card_label": "ECE",
                    "value": _f(r["ece"], 3)})
        out.append({"card_id": "stt-confidence", "card_label": "AUROC",
                    "value": _f(r["auroc"], 3)})
        out.append({"card_id": "stt-confidence", "card_label": "Brier Score",
                    "value": _f(r["brier_score"], 3)})
        out.append({"card_id": "stt-confidence", "card_label": "Words Evaluated",
                    "value": f"{float(r['n_words_evaluated']) / 1000:.1f}"})
        out.append({"card_id": "stt-confidence", "card_label": "Deletion Rate",
                    "value": f"{float(r['deletion_rate']) * 100:.1f}"})

    # ── Test 2.1 Naturalness ─────────────────────────────────────────────
    rows = _read_csv(results_dir / "tts/naturalness/summary.csv")
    if rows:
        r = rows[0]
        for csv_col, row_label in (
            ("utmos",       "UTMOS"),
            ("dnsmos_ovrl", "DNSMOS Overall (OVRL)"),
            ("dnsmos_sig",  "DNSMOS Signal (SIG)"),
            ("dnsmos_bak",  "DNSMOS Background (BAK)"),
        ):
            d = _pd(r[csv_col])
            if not d:
                continue
            for col_idx, k in enumerate(("mean", "p50", "p90", "std", "min", "max")):
                if k in d:
                    add("tts-naturalness", (row_label,), col_idx, f"{d[k]:.2f}")
        for label, csv_col in (
            ("DNSMOS Overall P50",    "dnsmos_ovrl"),
            ("DNSMOS Signal P50",     "dnsmos_sig"),
            ("DNSMOS Background P50", "dnsmos_bak"),
            ("UTMOS P50",             "utmos"),
        ):
            d = _pd(r[csv_col])
            if d:
                out.append({"card_id": "tts-naturalness", "card_label": label,
                            "value": f"{d['p50']:.2f}"})

    # ── Test 2.2 Intelligibility ─────────────────────────────────────────
    cat_label = {
        "harvard":        "Harvard Sentences",
        "conversational": "Conversational",
        "technical":      "Technical",
        "numbers":        "Numbers & Figures",
    }
    for r in _read_csv(results_dir / "tts/intelligibility/summary.csv"):
        label = cat_label.get(r["category"])
        if label is None:
            continue
        wer = _pct(r["round_trip_wer"])
        cer = _pct(r["round_trip_cer"]) if r.get("round_trip_cer") else wer
        add("tts-intelligibility", (label,), 0, wer)
        add("tts-intelligibility", (label,), 1, cer)

    # ── Test 2.3 Prosody ─────────────────────────────────────────────────
    rows = _read_csv(results_dir / "tts/prosody/summary.csv")
    if rows:
        r = rows[0]
        out.append({"card_id": "tts-prosody", "card_label": "F0 Mean",
                    "value": str(r["f0_mean_hz"])})
        out.append({"card_id": "tts-prosody", "card_label": "F0 Std Dev",
                    "value": str(r["f0_std_hz"])})
        out.append({"card_id": "tts-prosody", "card_label": "F0 Range",
                    "value": f"{r['f0_min_hz']}–{r['f0_max_hz']}"})
        out.append({"card_id": "tts-prosody", "card_label": "Speaking Rate",
                    "value": str(int(round(float(r["speaking_rate_wpm_mean"]))))})
        out.append({"card_id": "tts-prosody", "card_label": "Rate Std Dev",
                    "value": str(r["speaking_rate_wpm_std"])})

    # ── Test 2.5 Latency ─────────────────────────────────────────────────
    bucket_label = {
        "short_10_words":       "10 words",
        "medium_25_words":      "25 words",
        "long_75_words":        "75 words",
        "very_long_150_words":  "150 words",
        "extra_long_300_words": "300 words",
    }
    iface_label = {"grpc": "gRPC", "rest": "REST"}
    for r in _read_csv(results_dir / "tts/latency/summary.csv"):
        bucket = bucket_label.get(r["bucket"])
        iface = iface_label.get(r["interface"])
        if bucket is None or iface is None:
            continue
        ttfb = _pd(r["ttfb"])
        rtf = _pd(r["rtf"])
        elap = _pd(r["elapsed"])
        if ttfb: add("tts-latency", (bucket, iface), 0, _ms_no_comma(ttfb["p50"]))
        if rtf:  add("tts-latency", (bucket, iface), 1, _f(rtf["p50"], 3))
        if elap: add("tts-latency", (bucket, iface), 2, _ms(elap["p50"]))

    # ── Test 2.6 Concurrency ─────────────────────────────────────────────
    apr_baseline = {
        "1":  {"n_success": "50", "n_errors": "0", "err_rate": "0%",
               "rps": "1.55", "p50": "640 ms", "p99": "698 ms"},
        "5":  {"n_success": "50", "n_errors": "0", "err_rate": "0%",
               "rps": "7.03", "p50": "710 ms", "p99": "746 ms"},
        "10": {"n_success": "48", "n_errors": "2", "err_rate": "4%",
               "rps": "~11.7 *", "p50": "801 ms", "p99": "1,380 ms"},
        "20": {"n_success": "38", "n_errors": "12", "err_rate": "24%",
               "rps": "~14.5 *", "p50": "837 ms", "p99": "2,328 ms"},
    }
    for r in _read_csv(results_dir / "tts/concurrency/summary.csv"):
        n = r["concurrency"]
        b = apr_baseline.get(n)
        if not b:
            continue
        err_pct = f"{int(round(float(r['error_rate']) * 100))}%"

        # RPS handling: when rps_method='estimated' (June-style schema) OR when
        # error_rate > 0 with the older April schema (no rps_method), the raw
        # CSV RPS is wall-clock-contaminated and unreliable. In those cases we
        # render an annotated Little's-Law estimate ("~N *") instead of the
        # raw CSV value. Apply the same Little's-Law arithmetic:
        #   eff_concurrency = (n_success / n_calls) × N
        #   est_rps         = eff_concurrency / mean_latency_of_successful
        # which the test script already records (`rps` field) when method is
        # 'estimated'.
        method = r.get("rps_method", "").strip().lower()
        err_rate = float(r["error_rate"])
        unreliable = (
            method == "estimated" or
            (not method and err_rate > 0)  # older April schema fallback
        )
        if unreliable:
            try:
                eff = (float(r["n_success"]) / float(r["n_calls"])) * float(n)
                mean_lat = float(r["latency_mean"])
                est = eff / mean_lat if mean_lat else 0.0
                rps_val = f"~{est:.1f} *"
            except (ValueError, ZeroDivisionError):
                rps_val = str(r["rps"])
        else:
            rps_val = str(r["rps"])

        for anchor_key, csv_val in (
            ("n_success", str(r["n_success"])),
            ("n_errors",  str(r["n_errors"])),
            ("err_rate",  err_pct),
            ("rps",       rps_val),
            ("p50",       _ms(r["latency_p50"])),
            ("p99",       _ms(r["latency_p99"])),
        ):
            add("tts-concurrency", (n,), -1, csv_val,
                extra={"apr_anchor": b[anchor_key], "optional": True})

    # ── Test 2.8 Long-Form ───────────────────────────────────────────────
    for r in _read_csv(results_dir / "tts/long_form/summary.csv"):
        title = r["title"]
        add("tts-longform", (title,), 0, str(r["f0_drift_hz"]))
        add("tts-longform", (title,), 1, str(r["wpm_drift"]))
        add("tts-longform", (title,), 2, str(r["mean_speaker_sim"]))

    return out


# ── HTML patcher (numeric cells) ─────────────────────────────────────────


CARD_RE = re.compile(
    r'<div class="card"[^>]*\sid="([^"]+)"[^>]*>(.*?)</div>\s*</div>',
    re.DOTALL,
)
ROW_RE = re.compile(r"<tr>(.*?)</tr>", re.DOTALL)
TD_TEXT_RE = re.compile(r"<td[^>]*>(.*?)</td>", re.DOTALL)
RD_RE = re.compile(
    r'<span\s+class="rd[^"]*"\s+data-apr="([^"]*)"\s+data-jun="([^"]*)"[^>]*>'
    r"(.*?)</span>",
    re.DOTALL,
)
METRIC_CARD_RE = re.compile(
    r'<div class="metric-card[^"]*"[^>]*>\s*'
    r'<div class="label">([^<]+)</div>\s*'
    r'<div class="value">(.*?)</div>\s*</div>',
    re.DOTALL,
)
TAG_RE = re.compile(r"<[^>]+>")


def _strip_tags(s: str) -> str:
    return TAG_RE.sub("", s).strip()


def _normalise_label(s: str) -> str:
    s = (
        s.replace("&mdash;", "—").replace("&ndash;", "–")
         .replace("&nbsp;", " ").replace("&amp;", "&")
         .replace("&le;", "≤").replace("&ge;", "≥")
         .replace("&lt;", "<").replace("&gt;", ">")
    )
    return re.sub(r"\s+", " ", s).strip()


def apply_numeric_specs(
    html: str, run: str, specs: list[dict]
) -> tuple[str, list, list]:
    """Walk each card; for each spec belonging to that card, locate and patch
    the right ``data-{run}`` attribute. Returns (new_html, applied, missing)."""
    applied: list[tuple] = []
    missing: list[dict] = []

    def patch_card(card_id: str, body: str, card_specs: list[dict]) -> str:
        row_specs = [s for s in card_specs if "row_key" in s]
        card_specs_l = [s for s in card_specs if "card_label" in s]

        def patch_row(m: re.Match) -> str:
            inner = m.group(1)
            tds = TD_TEXT_RE.findall(inner)
            if not tds:
                return m.group(0)
            cleaned = [_normalise_label(_strip_tags(t)) for t in tds]
            row_match: list[dict] = []
            for s in row_specs:
                k = s["row_key"]
                if len(cleaned) >= len(k) and tuple(cleaned[: len(k)]) == k:
                    row_match.append(s)
            if not row_match:
                return m.group(0)
            rd_iter = list(RD_RE.finditer(inner))
            replacements: list[tuple[int, int, str]] = []
            for s in row_match:
                anchor = s.get("apr_anchor")
                if anchor is not None:
                    matches = [rm for rm in rd_iter if rm.group(1) == anchor]
                    if not matches:
                        if not s.get("optional"):
                            missing.append({"reason": "anchor-not-found",
                                            "card_id": card_id, **s})
                        continue
                    rd_m = matches[0]
                else:
                    col = s["col"]
                    if col >= len(rd_iter):
                        if not s.get("optional"):
                            missing.append({"reason": "col-out-of-range",
                                            "card_id": card_id, **s})
                        continue
                    rd_m = rd_iter[col]
                apr_val, jun_val, _ = rd_m.group(1), rd_m.group(2), rd_m.group(3)
                old = jun_val if run == "jun" else apr_val
                new_val = s["value"]
                if old != new_val:
                    applied.append((card_id, s["row_key"], s["col"], old, new_val))
                new_apr = new_val if run == "apr" else apr_val
                new_jun = new_val if run == "jun" else jun_val
                disp = new_apr
                cls_match = re.match(r'<span\s+class="(rd[^"]*)"', rd_m.group(0))
                cls = cls_match.group(1) if cls_match else "rd"
                new_span = (
                    f'<span class="{cls}" '
                    f'data-apr="{new_apr}" data-jun="{new_jun}">{disp}</span>'
                )
                replacements.append((rd_m.start(), rd_m.end(), new_span))
            new_inner = inner
            for start, end, new_span in sorted(replacements, reverse=True):
                new_inner = new_inner[:start] + new_span + new_inner[end:]
            return f"<tr>{new_inner}</tr>"

        body = ROW_RE.sub(patch_row, body)

        def patch_metric_card(m: re.Match) -> str:
            label = _normalise_label(m.group(1))
            value_html = m.group(2)
            for s in card_specs_l:
                if _normalise_label(s["card_label"]) != label:
                    continue
                rd_m = RD_RE.search(value_html)
                if not rd_m:
                    missing.append({"reason": "no-rd-in-card",
                                    "card_id": card_id, **s})
                    return m.group(0)
                apr_val, jun_val, _ = rd_m.group(1), rd_m.group(2), rd_m.group(3)
                old = jun_val if run == "jun" else apr_val
                new_val = s["value"]
                if old != new_val:
                    applied.append((card_id, ("card", label), 0, old, new_val))
                new_apr = new_val if run == "apr" else apr_val
                new_jun = new_val if run == "jun" else jun_val
                disp = new_apr
                cls_match = re.match(r'<span\s+class="(rd[^"]*)"', rd_m.group(0))
                cls = cls_match.group(1) if cls_match else "rd"
                new_span = (
                    f'<span class="{cls}" '
                    f'data-apr="{new_apr}" data-jun="{new_jun}">{disp}</span>'
                )
                new_value_html = (
                    value_html[: rd_m.start()] + new_span + value_html[rd_m.end():]
                )
                return m.group(0).replace(value_html, new_value_html, 1)
            return m.group(0)

        body = METRIC_CARD_RE.sub(patch_metric_card, body)

        # Special: card_label specs embedded in <strong>label: <span class="rd">…
        for s in card_specs_l:
            label = _normalise_label(s["card_label"])
            simple = re.compile(
                r"(<strong>\s*" + re.escape(label) + r":\s*"
                r'<span\s+class="(rd[^"]*)"\s+'
                r'data-apr="([^"]*)"\s+data-jun="([^"]*)"[^>]*>)([^<]*)(</span>)',
                re.DOTALL,
            )

            def repl(mm, _s=s, _label=label):
                _, cls, apr_val, jun_val, _disp, _ = mm.group(1, 2, 3, 4, 5, 6)
                old = jun_val if run == "jun" else apr_val
                new_val = _s["value"]
                if old != new_val:
                    applied.append((card_id, ("strong", _label), 0, old, new_val))
                new_apr = new_val if run == "apr" else apr_val
                new_jun = new_val if run == "jun" else jun_val
                disp = new_apr
                return (
                    f'<strong>{_label}: <span class="{cls}" '
                    f'data-apr="{new_apr}" data-jun="{new_jun}">{disp}</span>'
                )
            body = simple.sub(repl, body)
        return body

    new_html_parts: list[str] = []
    last_end = 0
    for m in CARD_RE.finditer(html):
        card_id = m.group(1)
        card_specs = [s for s in specs if s["card_id"] == card_id]
        card_html = m.group(0)
        if card_specs:
            body = m.group(2)
            new_body = patch_card(card_id, body, card_specs)
            card_html = card_html.replace(body, new_body, 1)
        new_html_parts.append(html[last_end : m.start()])
        new_html_parts.append(card_html)
        last_end = m.end()
    new_html_parts.append(html[last_end:])
    return "".join(new_html_parts), applied, missing


