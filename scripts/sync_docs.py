def run_numeric_sync(*, html: str, run: str, results_dir: Path,
                     check: bool) -> tuple[str, bool]:
    """Returns (new_html, changed). Prints a summary to stdout."""
    print(f"\n──── Numeric sync ────")
    print(f"Reading specs from {results_dir} …")
    specs = build_numeric_specs(results_dir)
    print(f"  built {len(specs)} patch specs")

    new_html, applied, missing = apply_numeric_specs(html, run, specs)

    if missing:
        print(f"\n⚠ {len(missing)} spec(s) had no matching anchor:",
              file=sys.stderr)
        for m in missing[:10]:
            print(f"    {m}", file=sys.stderr)
        if len(missing) > 10:
            print(f"    … and {len(missing) - 10} more", file=sys.stderr)

    if not applied:
        print(f"  ✓ docs/index.html numeric cells already match {results_dir} "
              f"for run={run}.")
        return html, False

    print(f"\n{len(applied)} numeric value(s) "
          f"{'would be ' if check else ''}updated:")
    for card_id, key, col, old, new in applied[:30]:
        print(f"  [{card_id} {key} col={col}]  "
              f"data-{run}: {old!r:>16} → {new!r}")
    if len(applied) > 30:
        print(f"  … and {len(applied) - 30} more")

    return new_html, True


def run_info_box_regen(*, html: str, run: str, results_dir: Path,
                      tests_filter: list[str] | None, model: str,
                      check: bool) -> tuple[str, bool]:
    """Returns (new_html, changed). Prints a summary to stdout."""
    print(f"\n──── Info-box regeneration (Anthropic API) ────")
    selected = (
        [t for t in INFO_BOX_TESTS if t["card_id"] in tests_filter]
        if tests_filter else
        [t for t in INFO_BOX_TESTS if not t.get("skip")]
    )
    if not selected:
        print(f"ERROR: no matching tests for --test {tests_filter!r}",
              file=sys.stderr)
        print(f"Available card_ids: "
              f"{[t['card_id'] for t in INFO_BOX_TESTS]}", file=sys.stderr)
        return html, False

    print(f"Generating info-boxes for run={run!r}, "
          f"{len(selected)} test(s), model={model!r}…\n")

    results: list[tuple[dict, dict]] = []
    for test in selected:
        print(f"  → {test['card_id']:25s}  {test['label']}")
        outcome = _process_info_box(
            test, run=run, results_dir=results_dir,
            docs_html=html, model=model,
        )
        results.append((test, outcome))
        if outcome.get("error"):
            print(f"      ✗ {outcome['error']}")
            continue
        fab = outcome.get("fabricated") or []
        if fab:
            print(f"      ⚠ FABRICATION: {fab}")
        diff = _diff_summary(outcome.get("current_box"), outcome.get("html", ""))
        print(f"      ✓ generated  {diff}")

    if check:
        print()
        for test, outcome in results:
            if outcome.get("error"):
                continue
            print("─" * 76)
            print(f"  {test['card_id']}  ({test['label']})")
            if outcome.get("fabricated"):
                print(f"  ⚠ fabricated: {outcome['fabricated']}")
            print("─" * 76)
            print(outcome["html"])
            print()
        return html, any(not o.get("error") for _, o in results)

    new_html = html
    written: list[str] = []
    skipped: list[tuple[str, str]] = []

    for test, outcome in results:
        if outcome.get("error"):
            skipped.append((test["card_id"], outcome["error"]))
            continue
        if outcome.get("fabricated"):
            skipped.append((test["card_id"],
                            f"fabricated numbers: {outcome['fabricated']}"))
            continue
        new_html, err = _replace_info_box(new_html, test["card_id"], run,
                                           outcome["html"])
        if err:
            skipped.append((test["card_id"], err))
        else:
            written.append(test["card_id"])

    if skipped:
        print(f"\n⚠ {len(skipped)} test(s) skipped:")
        for cid, why in skipped:
            print(f"    {cid:25s}  {why}")

    print(f"\n  ✓ {len(written)} info-box(es) regenerated"
          + (f", {len(skipped)} skipped" if skipped else ""))
    return new_html, len(written) > 0


# ── CLI ──────────────────────────────────────────────────────────────────


def main() -> int:
    p = argparse.ArgumentParser(
        description=__doc__.split("\n\n")[0],
        formatter_class=argparse.RawDescriptionHelpFormatter,
    )
    p.add_argument("--run", choices=["apr", "jun"], required=True,
                   help="Which run column to update")
    p.add_argument("--results-dir", required=True,
                   help="Path to results/<run-id>/ containing the summary CSVs")
    p.add_argument("--docs-html", default="docs/index.html",
                   help="Path to the toggleable docs report (default: docs/index.html)")

    mode = p.add_mutually_exclusive_group()
    mode.add_argument("--check", action="store_true",
                      help="Preview changes; do not write")
    mode.add_argument("--apply", action="store_true",
                      help="Write changes to docs/index.html")

    p.add_argument("--skip-numbers", action="store_true",
                   help="Skip the numeric-cell sync pass")
    p.add_argument("--skip-info-boxes", action="store_true",
                   help="Skip the info-box regeneration pass (no API calls)")

    p.add_argument("--test", action="append", default=None,
                   help="Restrict info-box regen to specific card_id(s); "
                        "may be passed multiple times")
    p.add_argument("--model", default=DEFAULT_MODEL,
                   help=f"Anthropic model to use (default: {DEFAULT_MODEL})")

    args = p.parse_args()

    if not args.check and not args.apply:
        p.error("must pass either --check (preview) or --apply (write)")
    if args.skip_numbers and args.skip_info_boxes:
        p.error("--skip-numbers and --skip-info-boxes are mutually exclusive; "
                "nothing would run")

    results_dir = Path(args.results_dir)
    docs_html_path = Path(args.docs_html)
    if not results_dir.exists():
        print(f"ERROR: results dir not found: {results_dir}", file=sys.stderr)
        return 2
    if not docs_html_path.exists():
        print(f"ERROR: docs html not found: {docs_html_path}", file=sys.stderr)
        return 2

    html = docs_html_path.read_text()
    any_changes = False

    if not args.skip_numbers:
        html, changed = run_numeric_sync(
            html=html, run=args.run, results_dir=results_dir, check=args.check,
        )
        any_changes = any_changes or changed

    if not args.skip_info_boxes:
        html, changed = run_info_box_regen(
            html=html, run=args.run, results_dir=results_dir,
            tests_filter=args.test, model=args.model, check=args.check,
        )
        any_changes = any_changes or changed

    if args.check:
        print(f"\n(--check: no writes performed)")
        return 0

    if not any_changes:
        print(f"\n✓ nothing to write — docs/index.html already in sync.")
        return 0

    docs_html_path.write_text(html)
    print(f"\n✓ wrote {docs_html_path}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
