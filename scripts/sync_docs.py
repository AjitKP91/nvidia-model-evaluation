#!/usr/bin/env python3
"""sync_docs.py — sync docs/index.html with results/<run>/.

The toggleable evaluation report at ``docs/index.html`` shows two runs side-
by-side (April baseline + June re-evaluation). Each cell carries both runs'
data as ``data-apr="…" data-jun="…"`` attributes; the user toggles between
them with the buttons at the top of the page.

This script keeps that report in sync with the underlying eval results in two
ways:

1. **Numeric cells** — every WER, latency, percentile, and metric-card value
   is anchored to its CSV in ``results/<run>/`` and rewritten verbatim. Pure
   string operations, no API calls.

2. **Analysis info-boxes** — the prose paragraphs interpreting each test are
   regenerated by sending the test's CSV + run.log excerpts + the April box
   (as a style anchor) to the Anthropic Messages API. Numbers are validated
   against the source data to prevent fabrication.

Usage
-----
    # Numbers + info-boxes (default):
    python scripts/sync_docs.py --run jun --results-dir results/run-09-06-26 --apply

    # Numbers only — fast, deterministic, no API calls:
    python scripts/sync_docs.py --run jun --results-dir results/run-09-06-26 \\
        --apply --skip-info-boxes

    # Info-boxes only — uses Anthropic API:
    python scripts/sync_docs.py --run jun --results-dir results/run-09-06-26 \\
        --apply --skip-numbers

    # Preview both without writing:
    python scripts/sync_docs.py --run jun --results-dir results/run-09-06-26 --check

    # Sync the April column from a different run dir:
    python scripts/sync_docs.py --run apr --results-dir results/run-26-04-26 --apply

    # Restrict info-box regeneration to specific tests:
    python scripts/sync_docs.py --run jun --results-dir results/run-09-06-26 \\
        --apply --skip-numbers --test stt-accuracy --test tts-concurrency

Requirements
------------
- For info-box regeneration: ``ANTHROPIC_API_KEY`` env var must be set.
  The ``anthropic`` Python SDK is used if installed; otherwise the script
  falls back to a stdlib HTTP client against
  ``https://api.anthropic.com/v1/messages``.
- For numeric sync: only the Python stdlib (no extra deps).

Safety properties
-----------------
- ``--check`` previews everything without writing.
- ``--apply`` is required to mutate ``docs/index.html``.
- The fabrication check rejects any LLM-emitted info-box that contains a
  numeric token not present in the source CSV/log/anchor.
- Numeric sync is fully idempotent — running twice produces no changes the
  second time. Info-box regeneration is also idempotent in spirit
  (temperature=0) but may produce trivially different prose run-to-run.
"""
