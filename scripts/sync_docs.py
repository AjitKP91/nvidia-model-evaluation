# ── Validation (fabrication guard) ───────────────────────────────────────


_NUMBER_TOKEN_RE = re.compile(
    r"(?:\d{1,3}(?:,\d{3})+|\d+)(?:\.\d+)?(?:%|\s?ms|\s?Hz|\s?WPM|\s?pp|\s?dB)?"
)


def _extract_numbers(text: str) -> set[str]:
    cleaned = re.sub(r"&\w+;", " ", text)
    return set(m.group(0).strip() for m in _NUMBER_TOKEN_RE.finditer(cleaned))


def _expand_csv_for_validator(csv_blob: str) -> str:
    """Expand any embedded percentile dicts in the CSV blob to also include
    short-precision representations of every numeric value. The validator
    matches on exact-round-trip-preserving variants, so a CSV cell like
    ``{'p50': 4.214194059, 'p90': 4.355814552}`` would otherwise reject
    legitimate citations of 4.21 / 4.36 (which the LLM rounds to 2 decimals)."""
    extra: list[str] = []
    # Find every floating-point literal in the CSV (including those inside
    # dict-as-string cells) and emit short-precision variants.
    for m in re.finditer(r"[-+]?\d+\.\d{3,}", csv_blob):
        try:
            v = float(m.group(0))
        except ValueError:
            continue
        for decimals in (1, 2, 3, 4):
            extra.append(f"{v:.{decimals}f}")
            extra.append(f"{v * 100:.{decimals}f}")
            extra.append(f"{v * 1000:.{decimals}f}")
            extra.append(f"{round(v * 1000):,}")  # comma-grouped ms
            extra.append(f"{round(v * 1000)}")
    return csv_blob + "\n" + " ".join(extra)


def _derive_counts(test: dict, results_dir: Path) -> str:
    """Derive aggregate counts from a test's CSV that the LLM is likely to
    cite (e.g. "X of N categories at 100%"). These don't appear literally in
    the CSV — they're computed from rows — so the validator otherwise rejects
    them. Returns a string of derived numbers to splice into the validator's
    source-text view."""
    derived: list[str] = []
    cid = test["card_id"]

    # Edge cases: "X of N categories at 100%"
    if cid == "tts-edge":
        rows = _read_csv(results_dir / "tts/edge_cases/summary.csv")
        n_total = len(rows)
        n_perfect = sum(1 for r in rows
                        if float(r.get("pass_rate", 0)) == 1.0)
        n_fail = sum(1 for r in rows
                     if float(r.get("pass_rate", 0)) < 1.0)
        derived += [str(n_total), str(n_perfect), str(n_fail)]

    # Long-form (TTS): X of 5 passages
    if cid == "tts-longform":
        rows = _read_csv(results_dir / "tts/long_form/summary.csv")
        derived.append(str(len(rows)))

    # Long-form (STT): segment counts
    if cid == "stt-longform":
        rows = _read_csv(results_dir / "stt/long_form/summary.csv")
        derived.append(str(len(rows)))
        n_perfect = sum(1 for r in rows if float(r.get("full_wer", 1)) == 0.0)
        derived.append(str(n_perfect))

    # Format robustness: pass/fail counts
    if cid == "stt-format":
        rows = _read_csv(results_dir / "stt/format_robustness/summary.csv")
        n_pass = sum(1 for r in rows if r.get("status") == "pass")
        n_fail = sum(1 for r in rows if r.get("status") == "fail")
        derived += [str(len(rows)), str(n_pass), str(n_fail)]

    # Accent: count of accents in pass / flag / fail bands
    if cid == "stt-accent":
        rows = _read_csv(results_dir / "stt/accent/summary.csv")
        derived.append(str(len(rows)))
        n_pass = sum(1 for r in rows if float(r.get("wer", 1)) < 0.10)
        derived.append(str(n_pass))

    return " ".join(derived)


def _validate_no_fabrication(html: str, sources: list[str]) -> list[str]:
    html_nums = _extract_numbers(html)
    src_nums: set[str] = set()
    for s in sources:
        src_nums |= _extract_numbers(s)

    # Also accept rounded forms of every source number. The LLM commonly
    # rounds e.g. 4.214194... to "4.21" or "4.2", or 194.3 Hz to "194 Hz".
    # Build a set of round-down/round-up integer + N-decimal forms for each
    # source number so legitimate citations are accepted.
    rounded_src: set[str] = set()
    for s_num in src_nums:
        try:
            f = float(s_num.replace(",", ""))
        except ValueError:
            continue
        for c in (f, f / 100.0, f / 1000.0, f * 100.0, f * 1000.0):
            for decimals in range(5):
                fmt = f"{c:.{decimals}f}"
                rounded_src.add(fmt)
                if "." in fmt:
                    rounded_src.add(fmt.rstrip("0").rstrip("."))
            # Comma-grouped integer for thousands (concurrency P50 like 1,480)
            rounded_src.add(f"{round(c):,}")
            rounded_src.add(f"{round(c)}")
    src_nums |= rounded_src

    # CSVs hold raw floats (seconds, ratios, fractions); LLM output renders
    # them as ms / % / Hz / etc. We accept any equivalent representation that
    # PRESERVES the actual numeric value (no precision loss).
    def variants(tok: str) -> list[str]:
        bare = tok.strip()
        for suffix in (" ms", "ms", " Hz", "Hz", " WPM", "WPM",
                       " pp", "pp", " dB", "dB", " s", "s", "%"):
            if bare.endswith(suffix):
                bare = bare[: -len(suffix)].strip()
                break
        bare_nc = bare.replace(",", "")
        out = {tok, bare, bare_nc}
        try:
            f = float(bare_nc)
        except ValueError:
            return list(out)

        # Try unit-equivalent representations: × 1000 (s → ms), ÷ 1000
        # (ms → s), × 100 (fraction → %), ÷ 100 (% → fraction), and identity.
        # Only emit a format string if it round-trips back to the original
        # value at single-precision (1e-9 tolerance) — this prevents bogus
        # near-zero collapses like "14 ÷ 100 → 0".
        for c in (f, f / 100.0, f / 1000.0, f * 100.0, f * 1000.0):
            for decimals in range(7):
                fmt = f"{c:.{decimals}f}"
                try:
                    if abs(float(fmt) - c) <= 1e-9 + abs(c) * 1e-9:
                        out.add(fmt)
                        if "." in fmt:
                            stripped = fmt.rstrip("0").rstrip(".")
                            if stripped:
                                out.add(stripped)
                except ValueError:
                    pass
        return list(out)

    fabricated: list[str] = []
    for n in html_nums:
        v_set = set(variants(n))
        if v_set & src_nums:
            continue
        # Bare 1-digit ints are too ambiguous to flag (they're often counts
        # like "1 in 5" or fractional shortcuts). Common pass-band thresholds
        # are ALSO whitelisted because they appear in evaluation_plan.md
        # rather than the per-test CSV.
        if re.fullmatch(r"\d", n):
            continue
        if n in {"1.0", "0.0", "100%", "5%", "8%", "10%", "12%"}:
            continue
        fabricated.append(n)
    return fabricated


def _replace_info_box(docs_html: str, card_id: str, run: str,
                      new_box_html: str) -> tuple[str, str | None]:
    cm = _find_card_named(docs_html, card_id)
    if not cm:
        return docs_html, f"card {card_id!r} not found"
    cs, ce, card_html = cm
    box_loc = _find_info_box(card_html, run)
    if not box_loc:
        return docs_html, f"info-box for run={run!r} not found in card {card_id!r}"
    bs, be, _ = box_loc
    new_card_html = card_html[:bs] + new_box_html.strip() + card_html[be:]
    return docs_html[:cs] + new_card_html + docs_html[ce:], None


def _extract_april_anchor(docs_html: str, card_id: str) -> str | None:
    card = _find_card_named(docs_html, card_id)
    if not card:
        return None
    box = _find_info_box(card[2], "apr")
    return box[2] if box else None


def _extract_current_box(docs_html: str, card_id: str, run: str) -> str | None:
    card = _find_card_named(docs_html, card_id)
    if not card:
        return None
    box = _find_info_box(card[2], run)
    return box[2] if box else None


def _process_info_box(test: dict, *, run: str, results_dir: Path,
                      docs_html: str, model: str) -> dict:
    april = _extract_april_anchor(docs_html, test["card_id"])
    current = _extract_current_box(docs_html, test["card_id"], run)
    if not april:
        return {"error": f"no April anchor found for {test['card_id']!r}"}
    ctx = _bundle_context(test, results_dir)
    if not ctx["csv"]:
        return {"error": f"no CSV data found at {test['csv_paths']!r}"}
    prompt = _build_prompt(test, april, ctx, run)
    try:
        raw = call_anthropic(prompt, model=model)
    except Exception as e:
        return {"error": f"Anthropic API call failed: {e}"}
    raw = raw.strip()
    if raw.startswith("```"):
        raw = re.sub(r"^```\w*\n?", "", raw)
        raw = re.sub(r"\n?```$", "", raw)
    raw = raw.strip()
    if 'data-run="' + run + '"' not in raw:
        return {"error": f"output is not a valid info-box (missing data-run={run!r})",
                "html": raw, "april_anchor": april, "current_box": current}
    derived_counts = _derive_counts(test, results_dir)
    fabricated = _validate_no_fabrication(
        raw,
        # Sanitise the log before passing it to the validator: stripping
        # timestamps, progress bars, etc. prevents the LLM from accidentally
        # citing a number that happens to appear in a tqdm rate or HH:MM:SS
        # timestamp (e.g. "14 of 17 categories" matching "14:53:41").
        # `_expand_csv_for_validator` adds short-precision variants of every
        # dict-cell float so legitimate-but-rounded citations (e.g. "4.21"
        # from a CSV cell holding 4.214194...) are accepted.
        # `derived_counts` adds aggregate values the LLM legitimately needs
        # (e.g. "12 of 17 categories at 100%") that aren't literal CSV cells.
        [_expand_csv_for_validator(ctx["csv"]),
         _strip_log_noise(ctx["log"]),
         april,
         derived_counts],
    )
    return {"html": raw, "fabricated": fabricated,
            "april_anchor": april, "current_box": current}


def _diff_summary(old: str | None, new: str) -> str:
    if old is None:
        return "(no current box; new content)"
    if old.strip() == new.strip():
        return "(unchanged)"
    o = re.sub(r"\s+", " ", old.strip())
    n = re.sub(r"\s+", " ", new.strip())
    if len(o) == len(n):
        diff_chars = sum(1 for a, b in zip(o, n) if a != b)
        return f"(differs in ~{diff_chars} chars)"
    return f"(length: {len(o)} → {len(n)} chars)"


